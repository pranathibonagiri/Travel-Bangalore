import UIKit

class IMSViewController: UIViewController, UITableViewDataSource,UITableViewDelegate {

    let array1 = ["Address:","Opened:","Owner:","Hours:","Phone:"]
    let array2 = ["1, Sampige Rd, Malleshwaram West", "2010","Mantri Developers","10AM–11PM, Everyday","080 3016 000"]
    override func viewDidLoad() {
        super.viewDidLoad()
        
        // Do any additional setup after loading the view.
    }
    
    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell1 = tableView.dequeueReusableCell(withIdentifier: "mantricell") as! MantricellTableViewCell
        cell1.title.text = array1[indexPath.row]
        cell1.des.text = array2[indexPath.row]
        return cell1
    }
    
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return array1.count
    }
    
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        
    }

}
